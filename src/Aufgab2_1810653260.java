public class Aufgab2_1810653260
{
    public static void main (String[] args)
    {
        String name = "Kadir1!";// string kann alle zeichen haben
        int name = 25; //int kann nur ganze zahlen haben 32bit
        double name = 3.84; //double kann ganze zahlen mit gleitkommerdarstellung haben 64 bit
        boolean name = true; // boolean kann nur true oder false haben
        long name = 20l;// long kann hinter den zahlen buchstaben haben und trotzdem rechenoperationen durchführen 64bit
        char name='abc';//in einfachen anführungsstrichen 16bit
        byte name = -12345678; // 8bit
        short name = 1234; // 16bit
        float name = 2.8f;// ist ähnlich wie double kann aber gleitkommerdarstellung haben 32bit


    }
}
