public class Aufgabe5_1810653260
{
    public static void main(String[] args)
    {
        int zahl1 = 1;
        int zahl2 = 2;

        System.out.println("" + ++zahl1 + "\n" + ++zahl2);
        System.out.println("" + --zahl1 + "\n" + --zahl2);
        /* zahl1++ erhöht die Variable nach abruf und ++zahl1 erhöht die Variable vor abruf */
    }
}
